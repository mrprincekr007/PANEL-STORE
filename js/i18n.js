window.currentLang = localStorage.getItem('nexus_lang') || 'en';
window.currencyCode = localStorage.getItem('nexus_currency') || 'INR';
window.currencySymbol = window.currencyCode === 'USD' ? '$' : '₹';
window.usdtRate = parseFloat(localStorage.getItem('nexus_usdt_rate')) || 88;

window.setUsdtRate = function(rate) {
    window.usdtRate = parseFloat(rate) || 88;
    try { localStorage.setItem('nexus_usdt_rate', rate.toString()); } catch(e) {}
};

window.formatPrice = function(inrAmount) {
    const num = parseFloat(inrAmount) || 0;
    if (window.currencyCode === 'USD') {
        const usdt = num / window.usdtRate;
        return '$' + usdt.toFixed(2) + ' USDT';
    }
    return '₹' + num.toFixed(2);
};

window.formatPriceShort = function(inrAmount) {
    const num = parseFloat(inrAmount) || 0;
    if (window.currencyCode === 'USD') {
        const usdt = num / window.usdtRate;
        return '$' + usdt.toFixed(2);
    }
    return '₹' + num.toFixed(2);
};

window.applyCurrency = function(code) {
    const symbols = { INR: '₹', USD: '$' };
    window.currencyCode = code;
    window.currencySymbol = symbols[code] || '₹';
    try { localStorage.setItem('nexus_currency', code); } catch(e) {}
    document.querySelectorAll('[data-inr]').forEach(el => {
        const inr = parseFloat(el.getAttribute('data-inr'));
        if (!isNaN(inr)) el.textContent = window.formatPriceShort ? window.formatPriceShort(inr) : ('₹' + inr.toFixed(2));
    });
};

window.applyTheme = function(theme) {
    let link = document.getElementById('nexus-light-theme');
    const base = window.location.pathname.includes('/components/') ? '../../' : '';
    if (theme === 'light') {
        if (!link) {
            link = document.createElement('link');
            link.id = 'nexus-light-theme';
            link.rel = 'stylesheet';
            link.href = base + 'css/light-mode.css';
            document.head.appendChild(link);
        }
        document.documentElement.classList.add('light-mode');
    } else {
        if (link) link.remove();
        document.documentElement.classList.remove('light-mode');
    }
};

window.applyLanguage = function(lang) {
  lang = lang || window.currentLang || 'en';
  window.currentLang = lang;
  try { localStorage.setItem('nexus_lang', lang); } catch(e) {}
  const t = window.i18n[lang] || window.i18n.en;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if (el.getAttribute('data-i18n-type') === 'placeholder') {
          el.placeholder = t[key];
        }
      } else {
        el.textContent = t[key];
      }
    }
  });
};

document.addEventListener('DOMContentLoaded', () => {
  if (window.currentLang && window.currentLang !== 'en') {
    window.applyLanguage(window.currentLang);
  }
  const savedTheme = localStorage.getItem('nexus_theme');
  if (savedTheme === 'light' && window.applyTheme) {
    window.applyTheme('light');
  }
  const savedCurr = localStorage.getItem('nexus_currency');
  if (savedCurr && savedCurr !== window.currencyCode && window.applyCurrency) {
    window.applyCurrency(savedCurr);
  } else if (window.applyCurrency) {
    window.applyCurrency(window.currencyCode);
  }
});
